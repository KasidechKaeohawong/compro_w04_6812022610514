#include <stdio.h>  // ใช้สำหรับเรียกฟังก์ชัน printf() จาก standard I/O library

int main() {        // 'int' = Keyword, 'main' = Identifier, '()' = Operator

    printf("Hello,\n");  // 'printf' = Identifier , Comstant "Hello\n"
    printf("C Tokens!\n");  // 'printf' = Identifier , Comstant "C Tokens!\n"

    return 0;  // 'return' = Keyword, '0' = Constant, ';' = Operator
}
/*Token Type	ใช้อยู่ในโค้ด
Keyword	int, return
Identifier	main, printf
Constant	"Hello,\n", "C Tokens!\n", 0
Operator	(), ;, =   */